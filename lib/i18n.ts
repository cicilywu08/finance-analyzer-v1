/** Returns the localized category label. Falls back to original string if unknown. */
export function getCategoryLabel(locale: Locale, category: string | null): string {
  if (category == null || category === "") {
    return getMessage(locale, "category.Uncategorized");
  }
  const key = "category." + category;
  const value = messages[locale][key] ?? messages.en[key];
  return value ?? category;
}
